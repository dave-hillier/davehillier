// Points p1, p2, & p3 specified in counter clock-wise order
//
void calcNormal(float v[3][3], float out[3])
{
	float v1[3],v2[3];
	static const int x = 0;
	static const int y = 1;
	static const int z = 2;

	// Calculate two vectors from the three points
	v1[x] = v[0][x] - v[1][x];
	v1[y] = v[0][y] - v[1][y];
	v1[z] = v[0][z] - v[1][z];

	v2[x] = v[1][x] - v[2][x];
	v2[y] = v[1][y] - v[2][y];
	v2[z] = v[1][z] - v[2][z];

	// Take the cross product of the two vectors to get
	// the normal vector which will be stored in out
	out[x] = v1[y]*v2[z] - v1[z]*v2[y];
	out[y] = v1[z]*v2[x] - v1[x]*v2[z];
	out[z] = v1[x]*v2[y] - v1[y]*v2[x];

	// Normalize the vector (shorten length to one)
	ReduceToUnit(out);
}

int filelength (FILE *f)
{
	int             pos;
	int             end;

	pos = ftell (f);
	fseek (f, 0, SEEK_END);
	end = ftell (f);
	fseek (f, pos, SEEK_SET);

	return end;
}

byte *MyReadFile (char *name, int *size)
{
	FILE *f;
	byte *data = NULL;

	if (f = fopen(name, "r"))
	{
		*size = filelength(f);
		data = (byte *)malloc(sizeof(byte) * *size);
		if (data)
			fread (data, sizeof(byte), *size, f);
		return data;
	}
	else
	{
		ConPrintf("Error: Cant open %s\n", name);
		*size = 0;
		return NULL;
	}
}

// Find next mesh etc 
bool FindNextToken (FILE *f, const char *token, long from, long *start, long *end)
{
	char data[255];
	int open=0, close=0;
	bool found = false;
	int i;

	fseek(f, from, SEEK_SET);
	while (!feof(f))
	{  
		fscanf(f, "%s", &data);

		if (strcmp (token, data) == 0)
		{
			*start = ftell(f) - strlen(token);
			found = true;
			break;
		}
	}

	if (!found)
		return false;

	fscanf(f, "%s", &data);
	if (strcmp ("{", data) == 0)
		open++;

	

	while ((open != close) && (i = fscanf(f, "%s", &data)))
	{
		if (strcmp ("{", data) == 0)
			open++;
		else if (strcmp ("}", data) == 0)
			close++;
	} 
	*end = ftell(f);
	fseek (f, (*start), SEEK_SET);
	return found;
}

void AngleVectors (vec3_t angles, vec3_t forward, vec3_t right, vec3_t up)
{
	float		angle;
	float		sr, sp, sy, cr, cp, cy;
	
	angle = angles[YAW] * (M_PI*2 / 360);
	sy = sin(angle);
	cy = cos(angle);
	angle = angles[PITCH] * (M_PI*2 / 360);
	sp = sin(angle);
	cp = cos(angle);
	angle = angles[ROLL] * (M_PI*2 / 360);
	sr = sin(angle);
	cr = cos(angle);

	forward[0] = cp*cy;
	forward[1] = cp*sy;
	forward[2] = -sp;
	right[0] = (-1*sr*sp*cy+-1*cr*-sy);
	right[1] = (-1*sr*sp*sy+-1*cr*cy);
	right[2] = -1*sr*cp;
	up[0] = (cr*sp*cy+-sr*-sy);
	up[1] = (cr*sp*sy+-sr*cy);
	up[2] = cr*cp;
}

