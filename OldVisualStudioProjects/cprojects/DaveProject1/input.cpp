void Input::Update()
{
	MouseUpdate();

	Vector3 f;
	camera.GetDirection(f);

	if (keys[K_MOUSE1])

		camera.Translate(f);
	else if (keys[K_MOUSE2])
		camera.Translate(-f);
	else if (keys[K_ESCAPE])
		exit(0);

}
